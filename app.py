import streamlit as st
import pandas as pd

# عنوان التطبيق
st.set_page_config(page_title="خريطة فرص التوسع", layout="wide")
st.title("🗺️ نظام توصية التوسع الجغرافي")

st.info("""
**كيف يعمل النظام؟**
يقوم النموذج بتحليل 3 مؤشرات رئيسية:
1. 📈 نمو السجل التجاري
2. 👥 الكثافة السكانية
3. 🏗️ النشاط العمراني
""")

# البيانات
@st.cache_data
def get_opportunity_data():
    return pd.DataFrame([
        {"المنطقة": "جيزان", "نمو_الأعمال": 85, "الكثافة": 70, "الفرصة": 92, "السبب": "قرب ميناء الملك عبدالله + نمو سياحي"},
        {"المنطقة": "تبوك", "نمو_الأعمال": 75, "الكثافة": 60, "الفرصة": 88, "السبب": "مشروع نيوم + حركة تجارية متزايدة"},
        {"المنطقة": "الطائف", "نمو_الأعمال": 80, "الكثافة": 75, "الفرصة": 85, "السبب": "موسمية السياحة + كثافة سكانية عالية"},
        {"المنطقة": "حائل", "نمو_الأعمال": 60, "الكثافة": 50, "الفرصة": 70, "السبب": "موقع لوجستي متوسط"},
    ])

df = get_opportunity_data()

# العرض
col1, col2 = st.columns([1, 2])

with col1:
    st.subheader("🏆 أفضل 3 مناطق مقترحة")
    top_3 = df.nlargest(3, 'الفرصة')
    for index, row in top_3.iterrows():
        st.success(f"✅ {row['المنطقة']} (درجة الفرصة: {row['الفرصة']}%)")
        st.caption(f"💡 {row['السبب']}")

with col2:
    st.subheader("📊 تحليل البيانات")
    st.bar_chart(df.set_index('المنطقة')['الفرصة'])

# الخريطة
st.subheader("📍 خريطة الفرص")
st.map(pd.DataFrame({
    'lat': [16.88, 28.39, 21.28],
    'lon': [42.55, 36.57, 40.41],
    'size': [100, 80, 60]
}))

# زر التحميل
st.download_button('📥 تحميل التقرير', df.to_csv(index=False).encode('utf-8-sig'), 'recommendations.csv')